function ShowChat() {
	  var x = document.getElementById("iframe-container");
	  var y = document.getElementById("show-button");
		y.style.display = "none"
		x.style.display = "block";
	}
	function HideChat() {
	  var x = document.getElementById("iframe-container");
	  var y = document.getElementById("show-button");
	  x.style.display = "none";
	  y.style.display = "block";
	}